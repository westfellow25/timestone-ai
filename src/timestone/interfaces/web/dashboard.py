def show_overview(scenarios_data, results_data, twin_data):
    st.header(f"Executive Summary - {twin_data['company_name']}")

    if not results_data:
        st.warning("No simulation results found. Run `python -m src.simulation.monte_carlo` to generate them.")
        return

    results = results_data["results"]
    top = max(results, key=lambda r: r["success_probability"])
    avg_roi = sum(r["mean_roi"] for r in results) / len(results)
    high_conf = sum(1 for r in results if r["success_probability"] > 0.85)
    has_npv = any("mean_npv" in r for r in results)

    col1, col2, col3, col4 = st.columns(4)
    col1.metric("Best success probability", fmt_pct(top["success_probability"]), f"Scenario #{top['scenario_id']}")
    col2.metric("Avg ROI multiplier", f"{avg_roi:.1f}x", f"{len(results)} scenarios")
    col3.metric("High-confidence (>85%)", high_conf, f"{high_conf / len(results):.0%} of total")
    if has_npv:
        total_npv = sum(r.get("mean_npv", 0) for r in results)
        col4.metric("Avg NPV", fmt_money(total_npv / len(results)), "5-year horizon")
    else:
        col4.metric("Total simulations",
                    f"{results_data['simulation_parameters']['iterations'] * len(results):,}",
                    "Monte Carlo")

    st.markdown("---")

    col1, col2 = st.columns(2)
    with col1:
        st.subheader("Success probability vs ROI")
        df = pd.DataFrame([
            {
                "Scenario": (r["scenario_name"][:35] + "...") if len(r["scenario_name"]) > 35 else r["scenario_name"],
                "Success probability": r["success_probability"],
                "ROI multiplier": r["mean_roi"],
                "Risk score": r.get("risk_score", 1 - r["success_probability"]),
            }
            for r in results
        ])
        fig = px.scatter(
            df, x="Success probability", y="ROI multiplier",
            color="Risk score", color_continuous_scale="RdYlGn_r",
            hover_data=["Scenario"],
            title="All scenarios - success vs return",
        )
        fig.update_layout(height=400)
        st.plotly_chart(fig, use_container_width=True)

    with col2:
        st.subheader("Top 10 by success probability")
        top10 = sorted(results, key=lambda r: r["success_probability"], reverse=True)[:10]
        top_df = pd.DataFrame([
            {
                "Scenario": (r["scenario_name"][:35] + "...") if len(r["scenario_name"]) > 35 else r["scenario_name"],
                "Success %": r["success_probability"] * 100,
                "ROI multiplier": r["mean_roi"],
            }
            for r in top10
        ])
        fig = px.bar(
            top_df, x="Success %", y="Scenario", orientation="h",
            color="ROI multiplier", color_continuous_scale="Blues",
            title="Success probability (%)",
        )
        fig.update_layout(height=400, showlegend=False, yaxis=dict(autorange="reversed"))
        st.plotly_chart(fig, use_container_width=True)


def show_top_scenarios(results_data, scenarios_data=None):
    st.header("TOP-3 Recommended Scenarios")
    if not results_data:
        st.warning("No simulation results available.")
        return

    results = results_data["results"]
    top_3 = sorted(results, key=lambda r: r["success_probability"], reverse=True)[:3]

    for i, scenario in enumerate(top_3, 1):
        with st.expander(f"RANK #{i}: {scenario['scenario_name']}", expanded=(i == 1)):
            c1, c2, c3, c4 = st.columns(4)
            c1.metric("Success probability", fmt_pct(scenario["success_probability"]))
            c2.metric("Mean ROI", f"{scenario['mean_roi']:.1f}x")
            mean_npv = get_metric(scenario, "mean_npv")
            c3.metric("Mean NPV", fmt_money(mean_npv) if mean_npv is not None else "-")
            payback = get_metric(scenario, "payback_years_median")
            c4.metric("Median payback", f"{payback:.1f}y" if payback is not None else "-")

            c1, c2, c3 = st.columns(3)
            c1.metric("90% CI lower", f"{scenario['confidence_90_lower']:.1f}x")
            c2.metric("90% CI upper", f"{scenario['confidence_90_upper']:.1f}x")
            c3.metric("Risk score", f"{scenario.get('risk_score', 1 - scenario['success_probability']):.3f}")

            # Recommendation banner
            p = scenario["success_probability"]
            if p >= 0.90:
                st.success("**STRONG RECOMMENDATION** - high success probability, proceed with confidence.")
            elif p >= 0.70:
                st.info("**PROCEED WITH CAUTION** - good odds, but requires risk management.")
            else:
                st.warning("**PILOT FIRST** - moderate success probability, test before scaling.")

            # Show which real cases informed this scenario, if available
            if scenarios_data:
                matching_scen = next(
                    (s for s in scenarios_data.get("scenarios", [])
                     if s.get("id") == scenario["scenario_id"]),
                    None,
                )
                if matching_scen and matching_scen.get("based_on_cases"):
                    cases = matching_scen["based_on_cases"]
                    prior = matching_scen.get("empirical_prior") or {}
                    failure_rate = prior.get("failure_rate")
                    rev_prior = (prior.get("revenue_uplift") or {})
                    n_cases = rev_prior.get("n", 0)
                    with st.container():
                        st.markdown("**Based on real cases (empirical prior):**")
                        st.markdown(", ".join(f"`{c}`" for c in cases[:5]))
                        cols = st.columns(3)
                        if failure_rate is not None:
                            cols[0].metric("Empirical failure rate", fmt_pct(failure_rate),
                                           f"n={n_cases} similar cases")
                        if rev_prior.get("p10") is not None:
                            cols[1].metric("Revenue uplift P10", fmt_pct(rev_prior["p10"]))
                            cols[2].metric("Revenue uplift P90", fmt_pct(rev_prior["p90"]))


def show_all_results(results_data):
    st.header("All Simulation Results")
    if not results_data:
        st.warning("No simulation results available.")
        return

    results = results_data["results"]
    has_npv = any("mean_npv" in r for r in results)

    rows = []
    for r in results:
        row = {
            "ID": r["scenario_id"],
            "Scenario": r["scenario_name"],
            "Success %": f"{r['success_probability'] * 100:.1f}",
            "Mean ROI x": f"{r['mean_roi']:.2f}",
            "Median ROI x": f"{r['median_roi']:.2f}",
            "90% CI Lower": f"{r['confidence_90_lower']:.2f}",
            "90% CI Upper": f"{r['confidence_90_upper']:.2f}",
            "Risk Score": f"{r.get('risk_score', 1 - r['success_probability']):.3f}",
        }
        if has_npv:
            row["Mean NPV ($M)"] = f"{r.get('mean_npv', 0) / 1e6:.2f}"
            row["Payback (y)"] = f"{r.get('payback_years_median', 0):.1f}"
        rows.append(row)

    df = pd.DataFrame(rows)
    st.dataframe(df, use_container_width=True, height=600)

    st.download_button(
        "Download as CSV",
        data=df.to_csv(index=False),
        file_name="timestone_simulation_results.csv",
        mime="text/csv",
    )


def show_compare(companies: dict, current_label: str):
    st.header("Compare Scenarios Side-by-Side")
    other_labels = [n for n in companies if n != current_label]
    if not other_labels:
        st.info("Need at least two companies with simulation results to compare.")
        return

    col1, col2 = st.columns(2)
    with col1:
        st.subheader(current_label)
        _, _, results_a = load_company_data(
            str(companies[current_label]["twin_path"]),
            str(companies[current_label]["scenarios_path"]) if companies[current_label]["scenarios_path"] else None,
            str(companies[current_label]["results_path"]) if companies[current_label]["results_path"] else None,
        )
        _render_compare_column(results_a)

    with col2:
        choice = st.selectbox("Compare with...", other_labels, key="compare_select")
        st.subheader(choice)
        _, _, results_b = load_company_data(
            str(companies[choice]["twin_path"]),
            str(companies[choice]["scenarios_path"]) if companies[choice]["scenarios_path"] else None,
            str(companies[choice]["results_path"]) if companies[choice]["results_path"] else None,
        )
        _render_compare_column(results_b)


def _render_compare_column(results_data):
    if not results_data:
        st.info("No simulation results for this company yet.")
        return
    results = results_data["results"]
    avg_p = sum(r["success_probability"] for r in results) / len(results)
    avg_roi = sum(r["mean_roi"] for r in results) / len(results)
    st.metric("Avg success probability", fmt_pct(avg_p))
    st.metric("Avg ROI multiplier", f"{avg_roi:.1f}x")
    top = sorted(results, key=lambda r: r["success_probability"], reverse=True)[:3]
    for r in top:
        st.markdown(f"**#{r['scenario_id']} {r['scenario_name']}** - P={fmt_pct(r['success_probability'])}, ROI={r['mean_roi']:.1f}x")


def show_methodology(results_data):
    st.header("Simulation Methodology")
    if not results_data:
        st.warning("No simulation results loaded.")
        return

    params = results_data["simulation_parameters"]
    c1, c2 = st.columns(2)
    with c1:
        st.subheader("Monte Carlo parameters")
        st.code(f"""
Iterations per scenario : {params['iterations']:,}
Total scenarios         : {params['total_scenarios']}
Total simulations       : {params['iterations'] * params['total_scenarios']:,}
Horizon                 : {params.get('horizon_years', '?')}y
Discount rate           : {params.get('discount_rate', '?')}
Random seed             : {params['random_seed']}
        """)
        st.markdown("""
        ### Financial model
        - Capex paid in year 0
        - Benefits start after the implementation period
        - Adoption ramps up (40% / 70% / 95% / 100%)
        - `cost_reduction` applies to operating costs (not revenue)
        - NPV discounted at WACC (default 12%)
        """)

    with c2:
        st.subheader("Risk factors modeled")
        st.markdown("""
        - **Revenue variance** +/-15-40% per risk level (low/med/high)
        - **Cost variance** +/-10-35%
        - **Implementation delays** up to +80% for high-risk
        - **Cost overruns** up to +50%
        - **Market downturn** (8% prob, -30% revenue impact)
        - **Competitive response** (15% prob, -20% revenue impact)
        - **Execution failure** (5% prob, project lost)
        """)


