def fmt_pct(x, decimals=1):
    """Format a fractional value (e.g. 0.55) as percentage. Handles None gracefully."""
    if x is None:
        return "-"
    return f"{x * 100:.{decimals}f}%"


def fmt_money(x, scale="auto"):
    """Format a USD value with K/M/B scaling."""
    if x is None:
        return "-"
    abs_x = abs(x)
    if abs_x >= 1e9:
        return f"${x / 1e9:.1f}B"
    if abs_x >= 1e6:
        return f"${x / 1e6:.1f}M"
    if abs_x >= 1e3:
        return f"${x / 1e3:.1f}K"
    return f"${x:.0f}"


def get_metric(result: dict, key: str, default=None):
    """Back-compat: new schema has mean_npv/payback, old schema doesn't."""
    return result.get(key, default)


