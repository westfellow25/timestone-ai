REPO_ROOT = Path(__file__).resolve().parents[4]


def discover_companies() -> dict:
    """Look at every *_twin.json in the repo root and return a {label: {twin, scenarios, results}} map."""
    companies = {}
    for twin_path in sorted(REPO_ROOT.glob("*_twin.json")):
        # Skip files generated by the multi-company generator (those are templates only).
        try:
            with open(twin_path) as f:
                twin = json.load(f)
        except (json.JSONDecodeError, OSError):
            continue
        name = twin.get("company_name", twin_path.stem)
        # Try to locate matching scenarios + results by simple prefix heuristic
        prefix = twin_path.stem.replace("_twin", "").replace("_digital_twin", "")
        scenarios_path = REPO_ROOT / f"{prefix}_scenarios.json"
        results_path = REPO_ROOT / f"{prefix}_simulation_results.json"
        companies[name] = {
            "twin_path": twin_path,
            "scenarios_path": scenarios_path if scenarios_path.exists() else None,
            "results_path": results_path if results_path.exists() else None,
            "twin": twin,
        }
    # KTZ has weird naming - make sure ktz_digital_twin pairs with ktz_scenarios
    ktz_twin = REPO_ROOT / "ktz_digital_twin.json"
    if ktz_twin.exists():
        with open(ktz_twin) as f:
            twin = json.load(f)
        companies[twin.get("company_name", "KTZ")] = {
            "twin_path": ktz_twin,
            "scenarios_path": REPO_ROOT / "ktz_scenarios.json"
            if (REPO_ROOT / "ktz_scenarios.json").exists() else None,
            "results_path": REPO_ROOT / "ktz_simulation_results.json"
            if (REPO_ROOT / "ktz_simulation_results.json").exists() else None,
            "twin": twin,
        }
    return companies


@st.cache_data
def load_company_data(twin_str: str, scenarios_str: str | None, results_str: str | None):
    """Load company JSON bundle. Strings used so streamlit can cache paths."""
    twin = json.loads(Path(twin_str).read_text())
    scenarios = json.loads(Path(scenarios_str).read_text()) if scenarios_str else None
    results = json.loads(Path(results_str).read_text()) if results_str else None
    return twin, scenarios, results


