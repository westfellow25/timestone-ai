def main():
    st.markdown('<h1 class="main-header">TimeStone AI</h1>', unsafe_allow_html=True)
    st.markdown('<p class="subtitle">Predict business transformation outcomes before you commit</p>', unsafe_allow_html=True)
    st.markdown("---")

    companies = discover_companies()
    if not companies:
        st.error("No twin files found in repo root. Run the digital twin generator first.")
        return

    with st.sidebar:
        st.markdown("### Company")
        company_label = st.selectbox("Select a company:", list(companies.keys()))
        twin_str = str(companies[company_label]["twin_path"])
        scen_str = str(companies[company_label]["scenarios_path"]) if companies[company_label]["scenarios_path"] else None
        res_str = str(companies[company_label]["results_path"]) if companies[company_label]["results_path"] else None
        twin, scenarios, results = load_company_data(twin_str, scen_str, res_str)

        st.info(
            f"**{twin.get('company_name', company_label)}**\n\n"
            f"Industry: {twin['metrics'].get('industry', '?')}\n\n"
            f"Revenue: {fmt_money(twin['metrics']['revenue'])}\n\n"
            f"Scenarios: {scenarios['total_scenarios'] if scenarios else '-'}\n\n"
            f"Simulations: {(results['simulation_parameters']['iterations'] * len(results['results'])) if results else '-'}"
        )

        st.markdown("### Navigation")
        page = st.radio("Select view:", [
            "Overview",
            "Top scenarios",
            "All results",
            "Sensitivity",
            "Compare companies",
            "Methodology",
        ])

    if page == "Overview":
        show_overview(scenarios, results, twin)
    elif page == "Top scenarios":
        show_top_scenarios(results, scenarios)
    elif page == "All results":
        show_all_results(results)
    elif page == "Sensitivity":
        show_sensitivity(scenarios, twin)
    elif page == "Compare companies":
        show_compare(companies, company_label)
    elif page == "Methodology":
        show_methodology(results)


def show_sensitivity(scenarios_data, twin_data):
    st.header("Sensitivity Analysis (Tornado Chart)")
    if not scenarios_data:
        st.warning("No scenarios available.")
        return
    st.markdown(
        "For the selected scenario, each input parameter is varied within a +/-30% band "
        "around its baseline. The bars below show how much the projected NPV swings. "
        "Wider bar -> that assumption is the most fragile."
    )

    scenarios = scenarios_data["scenarios"]
    labels = {f"#{s['id']} {s['name']}": s for s in scenarios[:200]}  # cap dropdown
    pick = st.selectbox("Pick a scenario:", list(labels.keys()))
    chosen = labels[pick]

    rev = twin_data["metrics"]["revenue"]
    op_costs = twin_data["metrics"]["operating_costs"]

    try:
        from timestone.services.sensitivity import sensitivity_analysis
    except ImportError:
        st.error("Sensitivity module unavailable.")
        return

    with st.spinner("Running sensitivity analysis (this takes ~5 seconds)..."):
        rows = sensitivity_analysis(chosen, rev, op_costs, iterations=400)

    if not rows:
        st.info("No sensitivity rows generated.")
        return

    baseline = rows[0].baseline_npv
    st.metric("Baseline NPV", fmt_money(baseline))

    # Build tornado chart
    df = pd.DataFrame([
        {
            "Parameter": r.parameter,
            "Low value NPV": r.low_npv,
            "High value NPV": r.high_npv,
            "Swing ($)": r.swing,
        }
        for r in rows
    ])
    fig = go.Figure()
    for _, row in df.iterrows():
        fig.add_trace(go.Bar(
            y=[row["Parameter"]],
            x=[row["High value NPV"] - baseline],
            base=baseline,
            orientation="h",
            name=f"{row['Parameter']} (high)",
            marker_color="#22c55e",
            showlegend=False,
        ))
        fig.add_trace(go.Bar(
            y=[row["Parameter"]],
            x=[row["Low value NPV"] - baseline],
            base=baseline,
            orientation="h",
            name=f"{row['Parameter']} (low)",
            marker_color="#ef4444",
            showlegend=False,
        ))
    fig.add_vline(x=baseline, line_dash="dash", line_color="black")
    fig.update_layout(
        title="NPV swing by parameter (green = upside, red = downside)",
        xaxis_title="NPV ($)",
        barmode="overlay",
        height=400,
        yaxis=dict(autorange="reversed"),
    )
    st.plotly_chart(fig, use_container_width=True)

    st.dataframe(df, use_container_width=True)


if __name__ == "__main__":
    main()
